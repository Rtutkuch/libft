#include "libft.h"

char *ft_strdup(const char *s1)
{
	char	*string;
	int		i;

	i = 0;
	string = (char*)malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (string == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		string[i] = s1[i];
		i++;
	}
	string[i] = '\0';
	return (string);
}

#include "libft.h"

char	*ft_strncat(char *restrict s1, const char *restrict s2, size_t n)
{
	size_t i;

	i = 0;
	while (*s1 != '\0')
		*s1++;
	while (*s2 != '\0' && i < n)
	{
		*s1++ = *s2++;
		i++;
	}
	while (i < n)
		s1[i++]  = '\0';
	return (s1);
}
